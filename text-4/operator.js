// Arithmetic Operators

// Exponentiation operator ** 
console.log(10 ** 2);  	

// Modulus operator % 

console.log(10 % 2);

// Increment operator ++ 

let add = 20;
add++;
add++;
add++;
add++;
add++;
console.log(add);

// Decrement operator --

let decrement = 20;
decrement--;
decrement--;
decrement--;
decrement--;
console.log(decrement);

// Comparison Operators

//  += operator

let number = 20;
// number = number + number;
number += number;
console.log(number);

// -= operator 

let number1 = 20;
// number1 = number1 - number1;
number1 -= number1;
console.log(number1);

// *= operator

let agg = 20;
// agg = agg * agg;
agg *= agg;
console.log(agg);

// ==

let marks = 200;
console.log(marks == 200);
console.log(marks != 100);

// === 

let value = "40";
console.log(value === 40);

//  < and >

console.log(10 > 30);
console.log(10 < 30);
console.log(10 >= 30);
console.log(10 <= 30);
